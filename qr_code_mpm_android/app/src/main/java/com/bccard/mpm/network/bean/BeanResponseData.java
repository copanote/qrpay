package com.bccard.mpm.network.bean;

import java.io.Serializable;

public class BeanResponseData implements Serializable {
	private Boolean success = false;
	private String code = "";
	private String errorMsg = "";

	private BeanResponseResultData result = new BeanResponseResultData();
	private String msg = "";

	public Boolean getSuccess() {
		return success;
	}

	public void setSuccess(Boolean success) {
		this.success = success;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getErrorMsg() {
		return errorMsg;
	}

	public void setErrorMsg(String errorMsg) {
		this.errorMsg = errorMsg;
	}

	public BeanResponseResultData getResult() {
		return result;
	}

	public void setResult(BeanResponseResultData result) {
		this.result = result;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}
}
